      <div>
     <b>Sidney Crosby </b>
     </div>
     (* 7. srpna 1987, Halifax, Nové Skotsko, Kanada) je kanadský hokejový útočník. Hraje za klub Pittsburgh Penguins v NHL a na mezinárodní scéně reprezentuje Kanadu.
